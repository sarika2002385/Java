public class DataTypeMaxRanges {

    public static void main(String[] args) {
        // Displaying minimurange of each primitive data type
        System.out.println("Primitive Data Type Ranges:");
   
        System.out.println("Byte range:  " + Byte.MAX_VALUE  );
        System.out.println("Short range:  " + Short.MAX_VALUE );
        System.out.println("Integer range: " + Integer.MAX_VALUE );
        System.out.println("Long range: " + Long.MAX_VALUE );
        System.out.println("Float range:  " + Float.MAX_VALUE);
        System.out.println("Double range: " + Double.MAX_VALUE );
        System.out.println("Character range: " + (int) Character.MAX_VALUE );
     
    }
}