public class IncrementDecrement{
    public static void main(String args[]){
	  int num=87;
	 //declare the value before and after the increment
	 
     System.out.println("initial value:" +num);
	 
	 System.out.println("increment(++num):" +(++num));
	 
	 
	 //decrement
     System.out.println("decrement(--num):"+(--num));
	     }
	 
	  }
	 
    
  





