public class DataTypeMinRanges {

    public static void main(String[] args) {
        // Displaying minimum range of each primitive data type
        System.out.println("Primitive Data Type Ranges:");
   
        System.out.println("Byte range:  " + Byte.MIN_VALUE  );
        System.out.println("Short range:  " + Short.MIN_VALUE );
        System.out.println("Integer range: " + Integer.MIN_VALUE );
        System.out.println("Long range: " + Long.MIN_VALUE );
        System.out.println("Float range:  " + Float.MIN_VALUE);
        System.out.println("Double range: " + Double.MIN_VALUE );
        System.out.println("Character range: " + (int) Character.MIN_VALUE );
     
    }
}